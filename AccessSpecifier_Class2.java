package java_Projects;

public class AccessSpecifier_Class2 {

	public void add()
	{
		System.out.println("I am Public - Add");
	}
	
	private void sub()
	{
		System.out.println("I am Private - Sub");
	}
	
	protected void mul()
	{
		System.out.println("I am Protected - Mul");
	}
	
	void div()
	{
		System.out.println("I am Default - Div");
	}

}
