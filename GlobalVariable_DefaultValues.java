package java_Projects;

public class GlobalVariable_DefaultValues {
	
	static int a;
	static char c;
	static double d;
	static float f;
	static String s;
	static boolean b;
	
	
	
	public static void main(String[] args)
	{	
		
		System.out.println("Default values of Global from Static to Static");
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(f);
		System.out.println(s);
		System.out.println("There is no default value for Char");
		
	}

}
