package java_Projects;

public class TypeCasting_Pract 
{

	public static void main(String[] args) 
	{
		
		int a = 100;
		double d = a;
		System.out.println(d); // Primitive type casting widening 
		
		double b = 101.11;
		int c = (int) b; // widening typecasting
		System.out.println(c);
		

	}

}
